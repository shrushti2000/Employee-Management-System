//POJO FILE
class Employee{
private int id;
private double salary;
private String name;

public void setId(int id){
this.id=id;
}

public int getId(){
return id;
}

public void setName(String name){
this.name=name;
}

public String getName(){
return name;
}

public void setSalary(double salary){
this.salary=salary;
}


public double getSalary(){
return salary;
}
}

